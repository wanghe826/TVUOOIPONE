{
    [keys release];
    [bar_style release];
    [key_id release];
    [key_sound release];
    [key_type release];
    [left_x release];
    [style release];
    [name release];
    [x release];
    [y release];
    [tv_x release];
    [tv_y release];
    [show_bar release];
    [text release];
    [super dealloc];
}

@end