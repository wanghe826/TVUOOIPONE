@synthesize name;
@synthesize x;
@synthesize y;
@synthesize tv_x;
@synthesize tv_y;
@synthesize show_bar;
@synthesize text;

- (KeyBean*) init
{
    if(self = [super init])
    {
    }
    return self;
}

